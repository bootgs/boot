/**
 * List of MIME types to define the response format.
 *
 * @see https://developers.google.com/apps-script/reference/content/mime-type
 */
export enum ContentMimeType {

  /**
   * ATOM MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  ATOM = "application/atom+xml",

  /**
   * CSV MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  CSV = "text/csv",

  /**
   * ICAL MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  ICAL = "text/calendar",

  /**
   * JAVASCRIPT MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  JAVASCRIPT = "application/javascript",

  /**
   * JSON formatted data.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  JSON = "application/json",

  /**
   * RSS MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  RSS = "application/rss+xml",

  /**
   * Plain text.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  TEXT = "text/plain",

  /**
   * VCARD MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  VCARD = "text/vcard",

  /**
   * XML MIME type.
   *
   * @see https://developers.google.com/apps-script/reference/content/mime-type
   */
  XML = "application/xml",

  /**
   * HTML content.
   */
  HTML = "text/html"
}
