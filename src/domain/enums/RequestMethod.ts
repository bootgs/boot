/**
 * Enum of HTTP request methods.
 */
export enum RequestMethod {
  /**
   * GET method.
   */
  GET = "get",

  /**
   * POST method.
   */
  POST = "post",

  /**
   * PUT method.
   */
  PUT = "put",

  /**
   * DELETE method.
   */
  DELETE = "delete",

  /**
   * PATCH method.
   */
  PATCH = "patch",

  /**
   * OPTIONS method.
   */
  OPTIONS = "options",

  /**
   * HEAD method.
   */
  HEAD = "head"
}
